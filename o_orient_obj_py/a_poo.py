"""
Programação Orientada a Objetos - POO

- POO é um paradigma de programação que utiliza mapeamento de objetos
do mundo real para modelos computacionais.

- Paradigma de programação é a forma/metodológica utilizada para pensar/desenvolver sistemas.

Principais elementos da Orientação a Objetos
- Classe -> Modelo do objeto do mundo real sendo apresentado computacionalmente;
- Atributo -> Características do objeto;
- Método -> Comportamento do objeto (funções).
- Construtor -> Método especial utilizado para criar os objetos;
- Objeto -> Instância da classe.
"""

numero = 10
print(numero)
print(type(numero))

nome = 'Tinky-Winky'
print(nome)
print(type(nome))


class Produto:
    pass


ps5 = Produto()
print(ps5)
print(type(ps5))
